"""Инструмент агента: «Совет директоров» — оценка бизнес-идеи панелью персон.

Четыре независимых взгляда (параллельные вызовы) + синтез-вердикт.
"""
import asyncio

from anthropic import AsyncAnthropic

import config

_client = AsyncAnthropic(api_key=config.ANTHROPIC_API_KEY)

PERSONAS = [
    ("💰 Финансовый директор",
     "Ты опытный CFO. Оцени идею с точки зрения денег: юнит-экономика, инвестиции на "
     "старт, точка безубыточности, денежный поток, финансовые риски. Будь конкретен в "
     "цифрах-порядках, даже если они оценочные."),
    ("📣 Директор по маркетингу",
     "Ты CMO с опытом на итальянском рынке. Оцени идею со стороны рынка: кто клиент, "
     "какая боль, размер и динамика рынка, конкуренты, канал выхода, позиционирование. "
     "Учитывай специфику итальянских PMI и семейного бизнеса."),
    ("🦈 Венчурный инвестор",
     "Ты частный инвестор. Оцени идею как вложение: масштабируемость, защитимость "
     "(moat), команда из одного консультанта — плюс или минус, главный риск провала, "
     "что должно быть правдой, чтобы идея взлетела."),
    ("🧐 Скептичный клиент",
     "Ты владелец итальянской PMI (производство, 40 сотрудников) — потенциальный клиент. "
     "Ты занят и не любишь тратить деньги. Скажи честно: купил бы ты это? Что тебя "
     "останавливает? Что должно измениться, чтобы ты заплатил?"),
]

_COMMON = (
    "\n\nКонтекст: идею предлагает Юлия Йозефсон — независимый бизнес-консультант в "
    "Италии (Дезио), позиционирование «Prima i processi, poi l'AI», клиенты — итальянские "
    "PMI. Отвечай по-русски, структурно, 150–250 слов. Без комплиментов, сразу по делу. "
    "Заверши строку вердиктом: ЗА / ПРОТИВ / ЗА С УСЛОВИЯМИ."
)


async def _ask_persona(title: str, system: str, idea: str) -> str:
    resp = await _client.messages.create(
        model=config.MODEL_SMART,
        max_tokens=1500,
        thinking={"type": "disabled"},
        system=system + _COMMON,
        messages=[{"role": "user", "content": f"Бизнес-идея:\n{idea}"}],
    )
    text = "".join(b.text for b in resp.content if b.type == "text")
    return f"{title}\n{text}"


async def _board_review(inp: dict) -> str:
    idea = inp["idea"]
    opinions = await asyncio.gather(
        *[_ask_persona(t, s, idea) for t, s in PERSONAS],
        return_exceptions=True,
    )
    valid = [o for o in opinions if isinstance(o, str)]
    if not valid:
        return "Совет директоров не собрался: все вызовы завершились ошибкой. Попробуй ещё раз."

    synthesis_resp = await _client.messages.create(
        model=config.MODEL_SMART,
        max_tokens=1500,
        system=("Ты председатель совета директоров. Тебе передали мнения членов совета "
                "о бизнес-идее. Сведи их в решение: 1) общий вердикт (ЗА / ПРОТИВ / ЗА С "
                "УСЛОВИЯМИ) и главный аргумент; 2) ключевые условия успеха (до 3); "
                "3) главный риск; 4) три конкретных следующих шага для проверки идеи "
                "малой кровью. По-русски, кратко, без воды."),
        messages=[{"role": "user",
                   "content": f"Идея:\n{idea}\n\nМнения совета:\n\n" + "\n\n---\n\n".join(valid)}],
    )
    synthesis = "".join(b.text for b in synthesis_resp.content if b.type == "text")

    return ("🏛 СОВЕТ ДИРЕКТОРОВ\n\n"
            + "\n\n---\n\n".join(valid)
            + "\n\n═══════════════\n\n📋 РЕШЕНИЕ СОВЕТА\n\n" + synthesis)


